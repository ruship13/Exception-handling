package demo.oops;
//using constructor in abstract class
abstract class Person {
		String name;
		int age;
		float height;
		String city;
		String dist;
		public Person(String name, int age, float height, String city, String dist) {
			super();
			this.name = name;
			this.age = age;
			this.height = height;
			this.city = city;
			this.dist = dist;
		}

	

}
